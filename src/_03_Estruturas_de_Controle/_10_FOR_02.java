package _03_Estruturas_de_Controle;

public class _10_FOR_02 {
    public static void main(String[] args) {
        //mini desafio
        //fazer um FOR decrementativo de 2 em 2
        //começando do valor 10 até 0

        for (int num = 10; num <= 10 && num >= 0; num -= 2) {
            System.out.printf("i = %d\n", num);
        }

    }
}
